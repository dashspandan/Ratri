# Observation mode
# ---------------------------------------------------------------------------

# Phase convention:
#   Phase 0    = transit centre (planet in front of star)
#   Phase ±0.5 = occultation centre (planet behind star)
#   Positive phases: post-transit, planet moving away from observer
#   Negative phases: post-occultation, planet returning toward observer
OB = 'tm'


# ---------------------------------------------------------------------------
# Search parameters
# ---------------------------------------------------------------------------

T_SEARCH_START = Time('2026-10-01 00:00:00', scale='utc')
T_SEARCH_END   = Time('2028-09-30 00:00:00', scale='utc')

# Phase window
# Transit: symmetric window centred on phase 0
PHASE_MIN    = -0.13
PHASE_MAX    = 0.13

MIN_DURATION = 1.5    # minimum observable window in hours
AIRMASS_MIN  = 1.0
AIRMASS_MAX  = 2.0


# ---------------------------------------------------------------------------
# Orbital and system parameters
