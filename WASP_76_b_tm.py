from ratri import ratri_auto, _select_night, _OBSERVATORIES, _PWV_TIERS


# ---------------------------------------------------------------------------
# File paths
# ---------------------------------------------------------------------------

ATM_DIR       = './atm_transmission'
SPECTRA_CACHE = './spectra_cache'
PLANET_MODEL  = None
SAVEDIR_BASE  = './output/WASP_76_b_tm/'
NIGHTS_FILE   = './spectra_cache/WASP_76_b_tm_nights.json'


# ---------------------------------------------------------------------------
# Instrument
# ---------------------------------------------------------------------------

INST  = 'nlot_ccd_giano'
MODES = []
T_EXP = 300


